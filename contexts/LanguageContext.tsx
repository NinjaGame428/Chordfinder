export function LanguageProvider({ children }: { children: React.ReactNode }) {
  // Initialize with a safe default that works on both server and client
  const [language, setLanguage] = useState<'en' | 'fr'>('en'); // Default to English

  useEffect(() => {
    // Only run on client side
    if (typeof window === 'undefined' || typeof document === 'undefined') return;

    try {
      // Check for language in URL query params first (from middleware)
      const urlParams = new URLSearchParams(window.location.search);
      const urlLang = urlParams.get('lang') as 'en' | 'fr' | null;
      
      // Check for language in cookies (set by middleware)
      const cookieLang = document.cookie
        .split('; ')
        .find(row => row.startsWith('language='))
        ?.split('=')[1] as 'en' | 'fr' | undefined;
      
      // Check localStorage safely
      let storedLang: 'en' | 'fr' | null = null;
      try {
        storedLang = localStorage.getItem('language') as 'en' | 'fr' | null;
      } catch (e) {
        // localStorage might not be available
      }
      
      // Priority: URL param > Cookie > localStorage > Default (English)
      const savedLanguage = urlLang || cookieLang || storedLang;
      
      if (savedLanguage && (savedLanguage === 'en' || savedLanguage === 'fr')) {
        setLanguage(savedLanguage);
        // Only save to localStorage if not from URL/cookie to avoid unnecessary writes
        try {
          if (!urlLang && !cookieLang && storedLang !== savedLanguage) {
            localStorage.setItem('language', savedLanguage);
          }
        } catch (e) {
          // Ignore localStorage errors
        }
      } else {
        // Default to English for new users
        setLanguage('en');
        try {
          localStorage.setItem('language', 'en');
        } catch (e) {
          // Ignore localStorage errors
        }
      }
    } catch (error) {
      // If there's any error accessing browser APIs, default to English
      console.warn('Error initializing language:', error);
      setLanguage('en');
    }
  }, []);

  useEffect(() => {
    // Only run on client side
    if (typeof window === 'undefined') return;

    try {
      // Save language preference to localStorage
      localStorage.setItem('language', language);
    } catch (error) {
      console.warn('Error saving language preference:', error);
    }
  }, [language]);

  const t = (key: string): string => {
    // Always return a string, even if there's an error
    try {
      // Ensure language is valid - default to English if invalid
      const currentLang = (language === 'en' || language === 'fr') ? language : 'en';
      
      // Safely access translations object
      if (!translations || typeof translations !== 'object') {
        return key;
      }
      
      const langTranslations = translations[currentLang];
      
      if (!langTranslations || typeof langTranslations !== 'object') {
        return key;
      }
      
      // Direct key access - use Record type for safety
      const translation = (langTranslations as Record<string, string>)[key];
      
      // Return translation if found, otherwise return the key
      return translation || key;
    } catch (error) {
      // If any error occurs, return the key as fallback
      return key;
    }
  };

  // Ensure we always provide a valid context value
  const contextValue = {
    language: (language === 'en' || language === 'fr') ? language : 'en',
    setLanguage,
    t
  };

  return (
    <LanguageContext.Provider value={contextValue}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}